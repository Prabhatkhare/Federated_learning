# CONTRASTIVE LOSS (MOON-style)
# ==============================
def contrastive_loss(v, vg, vl):
    """
    v  : current local model projection
    vg : global (previous round) projection  — POSITIVE
    vl : previous local model projection     — NEGATIVE
    """
    v, vg, vl = map(lambda x: F.normalize(x, dim=-1), [v, vg, vl])
    sg = torch.sum(v * vg, dim=-1) / TAU        # sim(current, global)
    sl = torch.sum(v * vl, dim=-1) / TAU        # sim(current, prev_local)
    return (-torch.log(torch.exp(sg) /
            (torch.exp(sg) + torch.exp(sl) + 1e-8))).mean()

