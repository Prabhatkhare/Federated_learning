def local_train_fedavg(model, loader):
    """FedAvg local training: CE loss only."""
    ce = nn.CrossEntropyLoss()
    opt = torch.optim.Adam(model.parameters(), lr=LEARNING_RATE)
    model.train()
    for _ in range(LOCAL_EPOCHS):
        for x, y in loader:
            x, y = x.to(DEVICE, non_blocking=True), y.to(DEVICE, non_blocking=True)
            opt.zero_grad()
            loss = ce(model(x), y)
            loss.backward()
            opt.step()
    return model


def local_train_fedprox(model, global_model, loader):
    """FedProx local training: CE + proximal term (mu/2)||w - w_global||^2."""
    ce = nn.CrossEntropyLoss()
    opt = torch.optim.Adam(model.parameters(), lr=LEARNING_RATE)
    # Freeze a snapshot of global params (detached clones, kept on DEVICE)
    global_params = [p.detach().clone() for p in global_model.parameters()]
    model.train()
    for _ in range(LOCAL_EPOCHS):
        for x, y in loader:
            x, y = x.to(DEVICE, non_blocking=True), y.to(DEVICE, non_blocking=True)
            opt.zero_grad()
            loss = ce(model(x), y)
            prox = sum(((w - w_t) ** 2).sum()
                       for w, w_t in zip(model.parameters(), global_params))
            (loss + (MU_PROX / 2) * prox).backward()
            opt.step()
    return model


def local_train_fednova(model, loader):
    """FedNova local training: CE loss, returns (model, tau_i)."""
    ce = nn.CrossEntropyLoss()
    opt = torch.optim.Adam(model.parameters(), lr=LEARNING_RATE)
    tau_i = 0                   # count local gradient steps
    model.train()
    for _ in range(LOCAL_EPOCHS):
        for x, y in loader:
            x, y = x.to(DEVICE, non_blocking=True), y.to(DEVICE, non_blocking=True)
            opt.zero_grad()
            ce(model(x), y).backward()
            opt.step()
            tau_i += 1
    return model, tau_i


def local_train_fedcl(model, g_prev, l_prev, loader):
    """FedCL / MOON local training: CE + mu * contrastive_loss."""
    ce = nn.CrossEntropyLoss()
    opt = torch.optim.Adam(model.parameters(), lr=LEARNING_RATE)
    g_prev.eval()               # deterministic projections (no dropout noise)
    l_prev.eval()
    model.train()
    for _ in range(LOCAL_EPOCHS):
        for x, y in loader:
            x, y = x.to(DEVICE, non_blocking=True), y.to(DEVICE, non_blocking=True)
            opt.zero_grad()
            with torch.no_grad():
                _, vg, _ = g_prev(x, True)    # global projection (positive)
                _, vl, _ = l_prev(x, True)    # prev-local projection (negative)
            out, v, _ = model(x, True)        # current model outputs
            loss = ce(out, y) + MU * contrastive_loss(v, vg, vl)
            loss.backward()
            opt.step()
    return model

# ==============================
# AGGREGATION — Weighted Average (FedAvg / FedProx / FedCL)
# ==============================
def aggregate(global_m, locals_, sizes):
    """Standard weighted aggregation: w = sum(n_i/n * w_i)."""
    new = copy.deepcopy(global_m.state_dict())
    total = sum(sizes)
    for k in new:
        if new[k].dtype.is_floating_point:
            new[k] = sum(
                locals_[i].state_dict()[k].to(DEVICE) * (sizes[i] / total)
                for i in range(len(locals_))
            )
    global_m.load_state_dict(new)
    global_m.to(DEVICE)   # ensure model is on GPU after loading state
    return global_m

# ==============================
# AGGREGATION — FedNova (Normalized Averaging)
# ==============================
def aggregate_fednova(global_m, locals_, taus, sizes):
    """
    FedNova normalized aggregation (Wang et al., NeurIPS 2020).

    d_i   = (w_global - w_local_i) / tau_i      — normalized pseudo-gradient
    p_i   = n_i / n_total                        — data-proportional weight
    tau_eff = sum(p_i * tau_i)                   — effective number of steps
    w_new = w_global - tau_eff * sum(p_i * d_i)  — updated global model
    """
    total = sum(sizes)
    weights = [s / total for s in sizes]

    # Effective number of local steps
    tau_eff = sum(w * t for w, t in zip(weights, taus))

    global_sd = global_m.state_dict()
    new_sd = copy.deepcopy(global_sd)

    for k in new_sd:
        if new_sd[k].dtype.is_floating_point:
            # Ensure all tensors are on DEVICE for computation
            g_param = global_sd[k].to(DEVICE)
            # Weighted sum of normalized pseudo-gradients
            delta = sum(
                weights[i] * (g_param - locals_[i].state_dict()[k].to(DEVICE)) / taus[i]
                for i in range(len(locals_))
            )
            new_sd[k] = g_param - tau_eff * delta

    global_m.load_state_dict(new_sd)
    global_m.to(DEVICE)   # ensure model is on GPU after loading state
    return global_m

