class ShuffleNetFedCL(nn.Module):
    def __init__(self, num_classes):
        super().__init__()
        backbone = models.shufflenet_v2_x0_5(
            weights=models.ShuffleNet_V2_X0_5_Weights.DEFAULT
        )
        self.backbone = nn.Sequential(*list(backbone.children())[:-1])
        num_ftrs = backbone.fc.in_features

        self.dropout = nn.Dropout(DROPOUT_RATE)
        self.proj = nn.Sequential(                  # projection head (contrastive)
            nn.Linear(num_ftrs, PROJ_DIM),
            nn.ReLU(),
            nn.Linear(PROJ_DIM, PROJ_DIM)
        )
        self.cls = nn.Linear(num_ftrs, num_classes) # classification head

    def forward(self, x, return_features=False):
        x = self.backbone(x).mean([2, 3])           # global avg pooling
        feat = self.dropout(x)
        proj = self.proj(feat)
        out = self.cls(feat)
        if return_features:
            return out, proj, feat
        return out

