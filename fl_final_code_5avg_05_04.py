def dirichlet_split(dataset, num_clients, beta):
    """Split dataset among clients using Dirichlet distribution."""
    if isinstance(dataset, Subset):
        labels = np.array(dataset.dataset.targets)[dataset.indices]
        base_dataset = dataset.dataset
        indices_ref = dataset.indices
    else:
        labels = np.array(dataset.targets)
        base_dataset = dataset
        indices_ref = np.arange(len(dataset))

    client_indices = [[] for _ in range(num_clients)]

    for cls in np.unique(labels):
        cls_idx = np.where(labels == cls)[0]
        np.random.shuffle(cls_idx)
        proportions = np.random.dirichlet(np.repeat(beta, num_clients))
        splits = (np.cumsum(proportions) * len(cls_idx)).astype(int)[:-1]
        cls_splits = np.split(cls_idx, splits)

        for i, s in enumerate(cls_splits):
            client_indices[i].extend(indices_ref[j] for j in s)

    return [Subset(base_dataset, idxs) for idxs in client_indices]

