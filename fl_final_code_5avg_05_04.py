# FAIRFEDCS CLIENT SELECTION (Shi et al., IJCAI 2023)
# ==============================

def compute_client_reputation(global_model, local_model, val_loader):
    """
    Evaluate a client's contribution quality by comparing global-only
    accuracy vs accuracy after incorporating the client update.
    Returns 1 (positive contribution) or 0 (negative contribution).
    """
    global_model.eval()
    local_model.eval()
    g_correct, l_correct, total = 0, 0, 0
    with torch.no_grad():
        for x, y in val_loader:
            x, y = x.to(DEVICE, non_blocking=True), y.to(DEVICE, non_blocking=True)
            g_correct += (torch.argmax(global_model(x), 1) == y).sum().item()
            l_correct += (torch.argmax(local_model(x), 1) == y).sum().item()
            total += y.size(0)
    # Positive if local model improved over global
    return 1 if l_correct >= g_correct else 0


def fairfedcs_select(queues, reputations, num_select):
    """
    FairFedCS client selection (Shi et al., IJCAI 2023).

    Client Selection Index:  CSI_i = SIGMA * R_i + Q_i
      - R_i = alpha_i / (alpha_i + beta_i)   (Beta Reputation System)
      - Q_i = virtual queue (higher → client has been under-served)
      - SIGMA controls performance vs fairness trade-off

    Select top-k clients with highest CSI.
    """
    num_clients = len(queues)
    csi = np.zeros(num_clients)
    for i in range(num_clients):
        R_i = reputations[i]       # already computed: alpha/(alpha+beta)
        Q_i = queues[i]
        csi[i] = SIGMA * R_i + Q_i
    selected = np.argsort(csi)[-num_select:]
    return selected


def fairfedcs_update_queues(queues, selected_indices, num_clients):
    """
    Virtual queue update rule (Lyapunov-based):
      Q_i(t+1) = max( Q_i(t) - x_i(t) + c_i, 0 )

    Where:
      x_i(t) = 1 if client i was selected, else 0
      c_i    = FAIRFED_TARGET (target participation rate)
    """
    selected_set = set(selected_indices)
    for i in range(num_clients):
        x_i = 1.0 if i in selected_set else 0.0
        queues[i] = max(queues[i] - x_i + FAIRFED_TARGET, 0.0)
    return queues


def fairfedcs_update_reputation(alphas, betas, client_idx, contribution):
    """
    Beta Reputation System (BRS) update:
      If contribution == 1 (positive):  alpha_i += 1
      If contribution == 0 (negative):  beta_i  += 1
      Reputation R_i = alpha_i / (alpha_i + beta_i)
    """
    if contribution == 1:
        alphas[client_idx] += 1
    else:
        betas[client_idx] += 1
    return alphas[client_idx] / (alphas[client_idx] + betas[client_idx])

