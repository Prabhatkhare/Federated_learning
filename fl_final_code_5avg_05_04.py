def save_checkpoint(filepath, run, rnd, models_dict, prev_cl, prev_pf,
                    ff_state, all_test_runs, all_times_runs, csv_position):
    """Save full training state to disk for crash recovery."""
    state = {
        'run': run,
        'round': rnd,
        'models': {k: m.state_dict() for k, m in models_dict.items()},
        'prev_cl': [m.state_dict() for m in prev_cl],
        'prev_pf': [m.state_dict() for m in prev_pf],
        'ff_queues': ff_state['queues'].tolist(),
        'ff_alphas': ff_state['alphas'].tolist(),
        'ff_betas': ff_state['betas'].tolist(),
        'ff_reps': ff_state['reps'].tolist(),
        'all_test_runs': [(
            {k: list(v) for k, v in d.items()}
        ) for d in all_test_runs],
        'all_times_runs': all_times_runs,
        'csv_position': csv_position,
    }
    tmp = filepath + ".tmp"
    torch.save(state, tmp)
    os.replace(tmp, filepath)
    print(f"  [CKPT] Saved checkpoint: run={run}, round={rnd}")


def load_checkpoint(filepath, num_classes):
    """Load checkpoint. Returns state dict or None if no checkpoint exists."""
    if not os.path.exists(filepath):
        return None
    try:
        state = torch.load(filepath, map_location=DEVICE, weights_only=False)
        print(f"  [CKPT] Loaded checkpoint: run={state['run']}, round={state['round']}")
        return state
    except Exception as e:
        print(f"  [CKPT] Corrupt checkpoint, starting fresh: {e}")
        return None


